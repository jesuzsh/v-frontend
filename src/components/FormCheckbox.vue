<template>
  <div>
    <label>
      <input
        type="checkbox"
        :checked="modelValue"
        @input="update($event.target.checked)"
      >
      {{ label }}
    </label>
  </div>
</template>

<script>
import { defineComponent } from 'vue'
export default defineComponent({
  props: {   
    modelValue: { required: true },
    required: {
      type: Boolean,
      default: false
    },
    config: {
      type: Object,
      default: () => {}
    },
    label: {
      type: String,
      requied: true
    }
  },
  methods: {
    update (value) {
      this.$emit('update:modelValue', value)
    }
  }
})
</script>
